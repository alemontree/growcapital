	<article class="questions">
		<h2>What is your account minimum?</h2>
		<p>We do not have a set minimum account value. We prefer to look at the totality of your needs for advice and guidance in determining if we are a good fit.</p>
		
		<h2>Is Grow Capital Management a broker?</h2>
		<p>No, Grow Capital Management is not a broker. We do not sell any products or services. Our fees are 100% based on a percentage of the assets that we directly manage for you.</p>
		
		<h2>Is Grow Capital Management the custodian of my accounts?</h2>
		<p>No, Grow Capital Management is not a custodian. Charles Schwab &amp; Co., Inc. is the custodian of your assets, and your accounts are titled in your name, whether individual or in trust. You have access to your Schwab accounts online, electronically, and by monthly statements.</p>


		<h2>What is Grow Capital Management’s relationship with my Schwab account?</h2>
		<p>Our clients sign a Limited Power of Attorney, allowing Grow Capital Management to execute discretionary trades in your account, disburse money to you, and collect our investment management fees. That’s it. Any additional actions or powers require your approval by signature.</p>

		<h2>What are your investment management fees?</h2>
		<p>Grow Capital Management charges an annual percentage fee of 0.25-0.75% on assets directly managed by us, paid on a quarterly basis. All advice and guidance you receive on non-discretionary assets, tax planning, trust and estate, insurance review, retirement forecasts, and decision analysis is included in our fee</p>

		<h2>How often do we meet to review our portfolio?</h2>
		<p>As often as you like. We encourage frequent communication with our clients whether in person, electronically, by phone, or by video conference.</p>

		<h2>How do we get started?</h2>
		<p>Before you commit to hiring Grow Capital Management as your investment advisor, we would like the opportunity to learn more about your unique financial situation and your ESG preferences, and present our recommendations in a complimentary financial report.</p>
